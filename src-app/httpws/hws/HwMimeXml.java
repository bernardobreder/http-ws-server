	/**
	 * Retorna o mapa de todos os tipos
	 * 
	 * @return mapa de todos os tipos
	 */
	public Map<String, String> map() {
		TreeMap<String, String> map = new TreeMap<String, String>();
		try {
			ByteArrayInputStream in = new ByteArrayInputStream(Charset.ascii(toString()));
			DocumentBuilderFactory dbf = DocumentBuilderFactory.newInstance();
			DocumentBuilder db = dbf.newDocumentBuilder();
			Document doc = db.parse(in);
			NodeList childNodes = ((Element) doc.getFirstChild()).getChildNodes();
			for (int n = 0; n < childNodes.getLength(); n++) {
				try {
					Element node = (Element) childNodes.item(n);
					String key = node.getAttribute("extension");
					String value = node.getAttribute("type");
					map.put(key, value);
				} catch (Exception e) {
				}
			}
		} catch (IOException | ParserConfigurationException | SAXException e) {
		}
		return map;
	}
	
